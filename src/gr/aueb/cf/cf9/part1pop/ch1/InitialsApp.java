package gr.aueb.cf.cf9.part1pop.ch1;

/**
 * Prints the initials "X A" in block-style letters
 * using asterisks (*) on the standard output.
 */

public class InitialsApp {

    public static void main(String[] args) {
        System.out.println("*       *        *       ");
        System.out.println(" *     *        * *      ");
        System.out.println("  *   *        *   *     ");
        System.out.println("    *         *******    ");
        System.out.println("  *   *      *       *   ");
        System.out.println(" *     *    *         *  ");
        System.out.println("*       *  *           * ");
    }
}